using System;

namespace MobilePhone.Library
{
    /// <summary>
    /// Specifies constants that define battery types.
    /// </summary>
    public enum BatteryType
    {
        /// <summary>
        /// Lithium ion battery type.
        /// </summary>
        LiIon,

        /// <summary>
        /// Nickel-metal hydride battery type.
        /// </summary>
        NiMH,

        /// <summary>
        /// Nickel-cadmium battery type.
        /// </summary>
        NiCd,

        /// <summary>
        /// Lithium polymer battery type.
        /// </summary>
        LiPoly,

        /// <summary>
        /// Generic battery type.
        /// </summary>
        Generic
    }
}
